    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
       String username = txtUser.getText();
    String password = txtPassword.getText();

    if (username.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Username atau Password tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
    } 

    else if (!isPasswordValid(password)) {
        JOptionPane.showMessageDialog(null, 
            "Password harus memenuhi kriteria berikut:\n" +
            "- Minimal 8 karakter\n" +
            "- Mengandung minimal 1 huruf besar (A-Z)\n" +
            "- Mengandung minimal 1 huruf kecil (a-z)\n" +
            "- Mengandung minimal 1 angka (0-9)\n" +
            "- Mengandung minimal 1 simbol (@#$%^&+=!)\n",
            "Error",
            JOptionPane.ERROR_MESSAGE); 
    } 
    else {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBO_UASPU");
        EntityManager em = emf.createEntityManager();

        em.getTransaction().begin();
        Pw user = new Pw();
        user.setUsername(username);
        user.setPassword(password);

        em.persist(user);
        em.getTransaction().commit();
        em.close();
        emf.close();

        JOptionPane.showMessageDialog(null, "Akun berhasil dibuat!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

        login l = new login();
        l.setVisible(true);
        this.dispose();
    }
    }//GEN-LAST:event_btnCreateActionPerformed

