    private boolean isPasswordValid(String password) {
    // Regular expression untuk validasi password
    String regex = "^(?=.*[a-z])(?=.*[A-Z])(?=.*\\d)(?=.*[@#$%^&+=!])(?=\\S+$).{8,}$";
    return password.matches(regex);
}
